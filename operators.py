# Operators Review 
# The Available Operators in Python are the Following

# Addition
a = 2 + 2
print(a)

# Subtraction
s = 5 - 3
print(s)

# Multiplication
m = 5 * 4
print(m)

# Expoential
e = 2*32

#Formated Print Statements (F-Strings)
print(f"Two raised to the power of 32 is {e}")

# Regular (Floating-Point) Division
d = 5 / 2
print(d)

# Floor Division (Round to Whole Number)
fd = 7 // 4
print(fd)

# Modulo (Remainder) Operator
mod_5 = 5 % 2
print(mod_5)
